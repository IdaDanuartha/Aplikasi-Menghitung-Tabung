    class Tabung extends Form1 {
        double r, tinggi, volume, luasPermukaan; 
        double pi;
                
        double hitungVolume() {
            r = Double.parseDouble(rTabung.getText());
            tinggi = Double.parseDouble(tinggiTabung.getText());
            
            if(r % 7 == 0) {
               volume = (22 * r * r) / 7 * tinggi;
            } else {
                pi = 3.14;
                volume = pi * r * r * tinggi;
            }
                                    
            return volume;
        }
        
        double hitungLuasPermukaan() {
            r = Double.parseDouble(rTabung.getText());
            tinggi = Double.parseDouble(tinggiTabung.getText());
            
            if(r % 7 == 0) {
               luasPermukaan = (2 * 22 * r) / 7 *( r + tinggi);
            } else {
                pi = 3.14;
                luasPermukaan = pi * r * tinggi * (r + tinggi);
            }
            
            
            
            return luasPermukaan;
        }
    }
    
