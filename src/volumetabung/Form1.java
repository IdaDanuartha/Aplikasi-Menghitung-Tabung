    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Tabung tbg = new Tabung();
        
        String piTabung;
        double resultLuas, resultVolume;
        int getJari, getTinggi;
                
        resultLuas = tbg.hitungLuasPermukaan();
        resultVolume = tbg.hitungVolume();
        
        getJari = Integer.parseInt(rTabung.getText());
        getTinggi = Integer.parseInt(tinggiTabung.getText());
        
        if(getJari % 7 == 0) {
            piTabung = "22/7";
        } else {
            piTabung = "3.14";
        }
        
        
        JOptionPane.showMessageDialog(this, "L. Permukaan = 2 x pi x r x (r + t) \n"
        + "= 2 x " + piTabung + " x " + getJari + " x (" + getJari + " + " + getTinggi + ") \n"
        + "= " + resultLuas + "\n\n"
        + "Volume = pi x r x r x tinggi \n"
        + "= " + piTabung + " x " + getJari + " x " + getJari + " x " + getTinggi + "\n"
        + "= " + resultVolume);
    }//GEN-LAST:event_jButton3ActionPerformed

